$(function(){
	// 轮播图片自适应
	// 1.1获取窗口的宽度
	$(window).resize(function(){
	// 1.2设置临界值
	let $divider=$(window).width()>768;
	// 1.3获取所有的item
	let $item=$("#lk-carousel .item");

	// 1.4遍历
		$item.each(function(index,item){
			// 1.4.1取出图片路径
			// 1.4.2设置背景
			if ($divider){
				$(item).empty();
				let src=$(item).data("md-img");
				let imgurl="url("+src+")";
				$(item).css({"background-image":imgurl});
			}else {
				// $(item).css({"background-image":none});				
				let src=$(item).data("sm-img");
				let ele=$('<img src="'+src+'">');

				$(item).empty().append(ele);
			}	
		});
	// 选项卡自适应
	let $lipresentation=$("#lk-tab-card [role='presentation']");
	let $ul=$("#lk-tab-card .nav");

	let ulwidth=0;
	$lipresentation.each(function(index,item){
		ulwidth+=$(item).width();
	});
	let parentWidth=$ul.parent().width();
	if (ulwidth>parentWidth) {
		$ul.css({
			width: ulwidth+"px"
		});
	}
	else{
		$ul.css({
			width: "100%"
		});
	}

	});
	$(window).trigger("resize");

	// 工具提示
	$('[data-toggle="tooltip"]').tooltip();	

	// 点击滚动
	let allli=$("#lk-nav-toggle li");
	$(allli[0]).click(function(){
		$("html,body").animate({scrollTop:$(".about-us").offset().top},1000);
	});
	$(allli[1]).click(function(){
		$("html,body").animate({scrollTop:$("#lk-tab-card").offset().top},1000);
	});
	$(allli[2]).click(function(){
		$("html,body").animate({scrollTop:$(".about-lesson").offset().top},1000);
	});


})



















































































































































































































































































































































































































































































































































